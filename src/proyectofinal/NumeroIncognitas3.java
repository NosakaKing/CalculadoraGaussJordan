    public void imprimir() {
        for (pos2 = 0; pos2 < 3; pos2++) {
            for (pos = 0; pos < 4; pos++) {
                if (matriz[pos][pos2] % 1 == 0) {
                    if (pos == 3) {
                        jTextArea1.append("|  " + ent.format(matriz[pos][pos2]));
                    } else {
                        jTextArea1.append(ent.format(matriz[pos][pos2]) + "\t");
                    }

                } else {
                    if (pos == 3) {
                        jTextArea1.append("|" + df.format(matriz[pos][pos2]));
                    } else {
                        jTextArea1.append(df.format(matriz[pos][pos2]) + "\t");
                    }

                }
            }
            jTextArea1.append("\n");
        }
        jTextArea1.append("\n");
    }

    public void matrices() {
        matriz[0][0] = X1;
        matriz[0][1] = X2;
        matriz[0][2] = X3;
        matriz[1][0] = Y1;
        matriz[1][1] = Y2;
        matriz[1][2] = Y3;
        matriz[2][0] = Z1;
        matriz[2][1] = Z2;
        matriz[2][1] = Z2;
        matriz[2][2] = Z3;
        matriz[3][0] = R1;
        matriz[3][1] = R2;
        matriz[3][2] = R3;
    }

    public String formato(double valor) {
        if (valor % 1 == 0) {
            return ent.format(valor);
        } else {
            return df.format(valor);
        }
    }

